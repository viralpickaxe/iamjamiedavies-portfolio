            <h2>whatleads.to<span class="type black">CauseHub Project</span></h2>
            <h4>Visualise what leads to your aims in just 2 steps. A project by <a href="http://causehub.io" target=_blank>CauseHub.io</a></h4>
            <p>whatleads.to makes working together to make real change simple. It lets you visualise your aims and how you're going to achieve them really quickly. It's derived from the idea of a Theory of Change, which is a process where you define what your ultimate aim and then work backwards, setting out goals that will lead to it.</p>
            <p>It makes visualising your aims a breeze. By asking "What leads to this?" again and again, you build up a map of all your activities without having to draw it out yourself. Then, you can edit any step and share it with your team or anyone else.</p>
            <p class="whatidid">On this project I am in charge of programming the backend. In this case the backend is a REST API writtern in PHP. I also write a lot of the frontend code to both fetch data from the API and also to generate the visualisations.</p>
