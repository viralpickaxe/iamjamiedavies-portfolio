            <h2>Relations Tracker<span class="type blue">Hackathon Project</span></h2>
            <h4>Visualing the UKs relationship with other countries through data sources provided by the FCO. A winning hack @ <a href="http://hacks.rewiredstate.org/events/fcohack" target=_blank>FCOHack 2014</a></h4>
            <p>This project was conceived at the Rewired State Foreign Commonwealth Hack in 2014. This is an API for tracking and qualifying several variables relating to the UK's Diplomatic Relationships. The data is colour coded onto a map giving a visual scale for reflecting the overall strength of the country's relationship with the UK, relative to all other countries. You can select different datasets to include for consideration.</p>
            <p>Unfortunately, the data that was given to us was very poorly formatted. There were many inconsistencies with basic things like what countries were called. This made it pretty hard to generate a good score. After the hackathon I then went on and blogged about the experience in a post titled, <a href="https://medium.com/words-by-causehub-io/making-data-useful-2851ac7a9335" target=_blank>Making Data Useful</a>.</p>
            <p>However, the concept was proven and we won the prize for the <a href="http://hacks.rewiredstate.org/events/fcohack/relations-tracker" target=_blank>Best App showing the value of diplomacy</a>!</p>
            <p class="whatidid">I built the API that scraped data from data.gov.uk and the FCO's data sources. It then returned this data to the frontend so that it could be easily inserted into the map.</p>
